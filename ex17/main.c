#include <unistd.h>
#include <stdio.h>

int	ft_strcmp(char *s1, char *s2);

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(void)
{
	char	s1[256];
	char	s2[256];

	scanf("%s", s1);
	scanf("%s", s2);
	printf("comparison difference is: %d \n", ft_strcmp(s1, s2));
	return (0);
}
