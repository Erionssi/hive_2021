#include <unistd.h>
#include <stdio.h>

int	ft_strlen(char *str);

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(void)
{
	char	s1;

	scanf("%s", &s1);
	printf("String length is: %d \n", ft_strlen(&s1));
	return (0);
}
